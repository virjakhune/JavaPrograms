/**
 * Program to Display Alphabets (A to Z) using Loop
 * 21-04-2022
 * Author:Virja Khune
 */
public class DisplayAlphabets {
    public static void main(String[] args) {
        System.out.println("Alphabets are: ");
        for(char c='A'; c<='Z'; c++) {
            System.out.print(c+" ");
        }

    }
}

