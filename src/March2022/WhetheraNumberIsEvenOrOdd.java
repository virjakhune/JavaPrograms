public class WhetheraNumberIsEvenOrOdd {
    public static void main(String[] args) {
        int x= 10;

        if (x%2==0) {
            System.out.println(x+" is a Even number");
        } else {
            System.out.println(x+" is a odd number");
        }
    }
}

